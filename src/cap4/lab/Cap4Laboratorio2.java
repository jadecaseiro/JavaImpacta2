package cap4.lab;

public class Cap4Laboratorio2 {
	public static void main(String[] args) {
		String mes = "Julho";
		switch (mes) {
		case ("Janeiro"):

		case ("Maio"):

		case ("Março"):

		case ("Agosto"):

		case ("Outubro"):

		case ("Julho"):

		case ("Dezembro"):
			System.out.println(mes + " tem 31 dias");
			break;
		case ("Fevereiro"):
			System.out.println(mes + " tem 28 dias");
			break;
		case ("Abril"):

		case ("Junho"):

		case ("Setembro"):

		case ("Novembro"):
			System.out.println(mes + " tem 30 dias");
			break;

		default:
			System.out.println(mes + " não corresposde a mes");

		}
	}

}
