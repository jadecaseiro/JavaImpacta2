package cap4.lab;

public class ExercicioExtra2Cap4 {
	public static void main(String[] args) {
		for(int a=1; a<=10; a++ ){
			for (int b=1, c; b<=10; b++){
				c=a*b;	
				System.out.println("A tabuada de "+a+"x"+b +" = "+ c);
			}
			System.out.println( "---------------------------" );
		}
	}
}
