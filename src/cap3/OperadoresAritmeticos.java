package cap3;

public class OperadoresAritmeticos {
	public static void main(String[] args) {
		int a = 10;
		int b = 25;
		int c = 40;
		int d = 55;
		System.out.println(a+b);
		System.out.println(b-a);
		System.out.println(c*d);
		System.out.println(c/a);
		System.out.println(d%a);
	}
}
