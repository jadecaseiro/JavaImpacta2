package cap7;

public class Calculo {

	static double somar(double a, double b) {
		return a + b;
	}

	static double dividir(double a, double b) {
		return (a / b);
	}

	static double multiplicar(double a, double b) {
		return (a * b);
	}

	static double subtrair(double a, double b) {
		return a - b;
	}

}
